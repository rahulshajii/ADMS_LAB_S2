SET SERVEROUTPUT ON;
SET VERIFY OFF;


CREATE TABLE EMPLOYEE(
    EID NUMBER,
    ENAME VARCHAR2(20),
    SALARY NUMBER
);


INSERT INTO EMPLOYEE VALUES(1,'RAHUL',25000);
INSERT INTO EMPLOYEE VALUES(2,'AMAL',30000);
INSERT INTO EMPLOYEE VALUES(3,'VIMAL',28000);
INSERT INTO EMPLOYEE VALUES(4,'MALU',35000);

DECLARE


CURSOR emp_sc IS
SELECT EID,ENAME,SALARY FROM EMPLOYEE;


V_EID EMPLOYEE.EID%TYPE;
V_ENAME EMPLOYEE.ENAME%TYPE;
V_SALARY EMPLOYEE.SALARY%TYPE;

BEGIN


OPEN emp_sc;

LOOP

FETCH emp_sc INTO V_EID,V_ENAME,V_SALARY;

EXIT WHEN emp_sc%NOTFOUND;

DBMS_OUTPUT.PUT_LINE(
'EID : '||V_EID||
' NAME : '||V_ENAME||
' SALARY : '||V_SALARY
);

END LOOP;

CLOSE emp_sc;

END;
